BEGIN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'sessions' AND column_name = 'boundary_type') THEN
        ALTER TABLE sessions ADD COLUMN boundary_type TEXT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'sessions' AND column_name = 'boundary_probability') THEN
        ALTER TABLE sessions ADD COLUMN boundary_probability FLOAT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'sessions' AND column_name = 'boundary_signals') THEN
        ALTER TABLE sessions ADD COLUMN boundary_signals JSONB NOT NULL DEFAULT '{}';
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'sessions' AND column_name = 'previous_session_id') THEN
        ALTER TABLE sessions ADD COLUMN previous_session_id TEXT REFERENCES sessions(session_id) ON DELETE SET NULL;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'sessions' AND column_name = 'continuity_score') THEN
        ALTER TABLE sessions ADD COLUMN continuity_score FLOAT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'sessions' AND column_name = 'cognitive_metadata') THEN
        ALTER TABLE sessions ADD COLUMN cognitive_metadata JSONB NOT NULL DEFAULT '{}';
    END IF;
END $$;

