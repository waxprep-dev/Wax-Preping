-- =============================================================================
-- WaxPrep v3.0 — Cognitive Memory Architecture Migration
-- =============================================================================
-- Architecture Decision: PostgreSQL-native graph layer
-- Rationale: Zero new infrastructure. Existing pgvector + JSONB handles
-- embeddings and flexible properties. Recursive CTEs provide traversal.
-- Neo4j adapter defined in code for future swap when query complexity grows.
-- =============================================================================

-- =============================================================================
-- BREAKTHROUGH 1: DUAL-PROCESS SESSION SEGMENTATION
-- =============================================================================

-- Segmentation configuration: learned weights and thresholds
-- student_id = NULL means global default; per-student rows override
CREATE TABLE IF NOT EXISTS segmentation_config (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    student_id TEXT, -- NULL = global default
    weights JSONB NOT NULL DEFAULT '{
        "topic_drift": 0.35,
        "emotional": 0.25,
        "cognitive": 0.25,
        "time": 0.10,
        "pedagogical": 0.05
    }',
    thresholds JSONB NOT NULL DEFAULT '{
        "boundary": 0.6,
        "time_gap": 30,
        "system1_trigger": 0.3
    }',
    features JSONB NOT NULL DEFAULT '{
        "use_embedding_drift": true,
        "use_emotional_delta": true,
        "use_lexical_shift": true,
        "use_cognitive_task_detection": true,
        "use_pedagogical_transition": true
    }',
    model_config JSONB NOT NULL DEFAULT '{
        "system1_model_tier": "fast",
        "system2_model_tier": "smart"
    }',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(student_id)
);

CREATE INDEX IF NOT EXISTS idx_segmentation_config_student ON segmentation_config(student_id);

-- Session boundaries log (for learning and observability)
CREATE TABLE IF NOT EXISTS session_boundaries (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    student_id TEXT NOT NULL,
    previous_session_id TEXT,
    new_session_id TEXT NOT NULL,
    boundary_type TEXT NOT NULL, -- dynamically generated by LLM, not enum
    boundary_probability FLOAT NOT NULL,
    boundary_signals JSONB NOT NULL DEFAULT '{}',
    llm_reasoning TEXT,
    was_correct BOOLEAN, -- human feedback or post-hoc validation
    detected_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_boundaries_student ON session_boundaries(student_id, detected_at DESC);
CREATE INDEX IF NOT EXISTS idx_boundaries_type ON session_boundaries(boundary_type);

